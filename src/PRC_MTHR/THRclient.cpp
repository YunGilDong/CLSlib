//------------------------------------------------------------------------------
// THRclient
//------------------------------------------------------------------------------
void *THRclient(void *data)
{
	bool initOK=true;
	CLSprcmthr *info;
	CLSthreadC *pThread = NULL;

	// 작업 환경 초기화
	info = (CLSprcmthr *)data;
	pThread = info->Thread;
	initOK = TCLinitEnv(info);

	while (initOK)
	{
		printf("#THRclient RUN#");
		pThread->MarkTime();

		pThread->UpdateRunInfo();	// 실행 정보 갱신
		pThread->Pause(500);	// 500 msec
	}
	// 작업 환경 정리
	TCLclearEnv(info);
}