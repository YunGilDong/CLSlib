void CLSprcthrc::Delete(void)
{
	if (Thread != NULL)
	{
		if (Thread->Active)
		{
			Thread->Stop(true);			
			printf("Thread Stop\n");
		}
		delete Thread;
		Thread = NULL;
	}
	Active = false;
}
//------------------------------------------------------------------------------
// Init
//------------------------------------------------------------------------------
void CLSprcthrc::Init(void)
{
	ID = -1;
	Thread = NULL;
	Active = false;
}
//------------------------------------------------------------------------------
void CLSprcthrc::Init(DB_PRCTHRC *pInfo)
{
	ID = pInfo->id;
	sprintf(Mng.address, "%s", pInfo->address);
	SetName("PRCTHRC%3d", ID);
	Thread = NULL;
	InitState(pInfo);
	Active = false;
}
